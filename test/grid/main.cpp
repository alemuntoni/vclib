#include <iostream>

#include <vclib/space/grid.h>

int main()
{
	vcl::Grid3<double> g(vcl::Point3d(0,0,0), vcl::Point3d(1,1,1), vcl::Point3<uint>(10, 10, 10));

	vcl::Point3<uint> first(2,2,2), last(5, 4, 7);

	for (const auto& c : g.cells(first, last))
		std::cerr << c << "\n";

	return 0;
}
